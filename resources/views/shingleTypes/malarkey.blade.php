    <h3>Summary</h3>
    <p>
        Malarkey Roofing Products makes a line of <a href="/shingles/asphalt.html">3-tab</a> and laminated 
        shingles. Malarkey 
        has added a feature to their shingles that they refer to as The Zone™. The 
        Zone™ features a larger nailing area per shingle than some competitors' 
        shingles. Malarkey shingles with The Zone™ have a 1 3/4" nailing area, 
        compared to the usual 1/2" nail area on most shingles. 
    </p>
    <p>
        Malarkey's laminated shingles are available in three series: the Legacy®, 
        the Northwest-XL™ and the Highlander-CS™. Malarkey also makes a 
        heavy-duty asphalt shingle marketed under the series name Alaska™. 
    </p>
    <p>
        Malarkey specializes in making roofing products for extreme weather conditions 
        (extreme cold or high winds). 
    </p>
    <h3>History</h3>
    <p>
        Malarkey Roofing Products has been manufacturing roofing products for residential 
        and commercial applications since 1956. Malarkey maintains two manufacturing 
        facilities, located in Portland, Oregon and Southgate, California.
    </p>
    <h3>Warranty</h3>
    <p>
        Malarkey offers a variety of warranties on their shingles, depending on the shingle 
        series. Malarkey offers limited warranties on shingles ranging from 20 to 40 years. 
        The warranty covers any manufacturer defect during the warranty period. It covers 
        repair and/or replacement of the shingles, excluding the labor. Malarkey also 
        extends algae resistance treatment to their shingles. Algae resistance warranties 
        vary by series, but generally is warranted against damage from five to 15 years. 
        Malarkey warranties can be transferred within a specified period of time to new owners. 
    </p>
    <p>
        We recommend that you inspect the warranty carefully to make sure that you understand 
        its terms. 
