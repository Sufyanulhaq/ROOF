    <h3>History</h3>
    <p>
        In the United States, Gerard's shingles are marketed under Gerard Roofing Technologies; in 
        Europe and Asia, the shingles are marketed under the Allmet product name. Gerard Roofing 
        Technologies began manufacturing metal shingles in 1971. Gerard's corporate headquarters 
        are based in Brea, California.  
    </p>
    <h3>Gerard's Warranty</h3>
    <p>
        Gerard roofing products carry a transferable, lifetime limited warranty. The warranty is transferable 
        to one subsequent owner. The roof is warranted for 50 years from the date of installation. The 
        warranty covers winds up to 120 mph and hail damage from hail stones up to 2 1/2 inches in diameter. 
        As with all products, we recommend you inspect the warranty carefully to make sure you understand 
        its terms. 
