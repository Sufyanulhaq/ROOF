    <h3>Product Summary</h3>
    <p>
        Gerard Roofing Technologies is the manufacturer of stone-coated metal roofing products and 
        metal roof panels. Gerard uses 26-gauge primed, galvanized steel. The shingles are coated 
        with stone granules. Gerard forms the metal shingles into shapes that closely resemble 
        traditional roofing products, such as wooden shake and clay tiles. They also offer a line 
        of roofing products that are plain, durable, metal panels. These panels feature raised seams 
        to prevent water leaks. 
    </p>
