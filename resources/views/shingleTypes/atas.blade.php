    <h3>History</h3>
    <p>
        Jacobus P. Bus, a siding and trim carpenter, founded Aluminum Trim and Shapes in 1963. In 1972 he opened a manufacturing plant 
        and began making residential home siding and commercial panels. With the expansion came the new name: ATAS Aluminum Corp. In 1985 
        the business moved from Rochester, N.Y., to Allentown, Pa. The company became known as ATAS International Inc. in 1995. 
