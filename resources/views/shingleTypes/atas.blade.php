    <h3>Product Lines</h3>
    <p>
        The Advanta is made from galvanized steel or copper, and was designed as an alternative to asphalt shingles. Its design simulates 
        depth and shadows. This shingle features an embossed stucco pattern and comes in one of 16 available solid or two-tone colors. 
    </p>
    <p>
        Castle Top shingles are diamond-shape tiles that create an unusual look. They are made from aluminum, copper or zinc. They may 
        be installed in a variety of colors to make designs. 
    <p>
        Rumba Shake shingles are a Bermuda-style panel made from anodized aluminum, steel or 16 ounce copper. The finish is smooth and 
        the application is designed for conventionally pitched roofs. The manufacturer notes the product can be installed over existing roofs. 
    </p>
    <p>
        The PermaShake I and II shingles have the appearance of wood shingles, but are actually steel. Unlike wood, they resist algae 
        growth, rot and other maintenance issues associated with natural products. 
    </p>
    <p>
        Standing Seam Shingles come in aluminum or copper. They are designed to be installed in a staggered pattern to create a historic look. 
    </p>
