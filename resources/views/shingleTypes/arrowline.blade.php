    <h3>A Summary of EDCO Products</h3>
    <p>
        ArrowLine metal shingles are available in two different styles: "Shakes" shingles offer a traditional look, while "slate" shingles provide a 
	European look for buildings. Both types of shingles are rated to withstand winds up to 160 mph and, according to the manufacturer, have the 
	highest rated hail-resistance available. The shingles are available with embossed patterns and a Kynar 500 finish. Slate shingles are available 
	in 11 colors and shake shingles come in four colors. The products hold an Energy Star rating for energy efficiency. 
    </p>
    <h3>History of EDCO and ArrowLine Metal Shingles</h3>
    <p>
        EDCO Products was founded in 1946 in Hopkins, Minn. The company began manufacturing insulated asphalt siding for new homes after World War II 
	and has grown to produce a full line of permanent exterior products. The company notes that it has received several awards from Metal Roofing 
	Magazine, including its Top 10 Product award and its Metal of Honor. EDCO states that the company's focus is on innovation and a 
	"commitment to excellence."
    </p>
    <h3>EDCO Warranty Information</h3>
    <p>
	Both types of EDCO ArrowLine metal shingles carry a full lifetime warranty. The company states that this warranty
	is not prorated and can be transferred to a new owner. In addition, the warranty provides fade protection for
	finish and color, as well as hail protection in the event of damage. 
    </p>
