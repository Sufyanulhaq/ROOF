    <h3>Product Line</h3>
    <p>
        Landmark shingles are a product line of 
        <a href="/shingles/brands/certainteed.html">CertainTeed Shingle Company</a>. Landmark 
        is CertainTeed's line of laminated shingles. Landmark shingles are made to resemble 
        high-end cedar shingles. The Landmark line features a two-piece random tab, which helps 
        to create a natural pattern. Landmark shingles have several options for warranties (30-, 
        40-, and 50-year or lifetime).
    </p>
    
    <p>
        CertainTeed shingles have consistently ranked highly with consumers and contractors. A 
        recent phone survey (a random sample of 200 subscribers to Professional Remodeler) 
        awarded CertainTeed the "Overall Best of Class" title for exterior products.
    </p>
    <h3>CertainTeed's History</h3>
    <p>
        Landmark is one of the product lines made by manufacturer CertainTeed.  CertainTeed began 
        making shingles in 1904 under the name General Roofing Manufacturing Company. In 1988, 
        the industrial holdings company Saint-Gobain acquired CertainTeed. The company is 
        headquartered in Valley Forge, Pennsylvania.
    </p>
    <h3>Warranty</h3>
    <p>
        Landmark shingles are warranted under CertainTeed. There are several warranties, depending 
        on which shingle you choose. Landmark shingles carry a minimum warranty to the original 
        property owner of 30 years. Some of the other Landmark shingles carry 40- and 50-year 
        warranties, while the TL series is warranted for the original owner's lifetime. The 
        warranty extends to any manufacture defects for the warranted period of time. CertainTeed 
        will repair replace the shingles during the warranty period, but will not cover the labor costs. 
    </p>
    <p>
        Included with the Landmark shingle is CertainTeed's SureStartâ„¢ protection warranty 
        which covers replacement or repair of Landmark shingles (including labor costs) for 5 
        years (10 years for the TL series) in the event that the shingles prove to have a 
        manufacturer's defect. 
    <p>
        Landmark shingles' warranty covers damage as a result of winds from 70 to 110 mph, 
        depending on the shingle. The wind warranty is available for five years on most 
        Landmark shingles, and 10 years on the TL series. 
    </p>
    <p>
        Landmark TL shingles are guaranteed against algae growth (though not mold or mildew growth) 
        for 15 years. If the shingles are affected by algae growth, CertainTeed may request that 
        the shingles be cleaned instead of replaced.
    </p>
    <p>
        CertainTeed's warranty will not cover defects as a result of improper installation, 
        roof ventilation, or inadequate maintenance. Review your product warranty carefully to 
        make sure you understand the terms. 
