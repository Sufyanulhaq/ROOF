    <h3>Summary</h3>
    <p>
        Pinnacle shingles are <a href="/shingles/brands/atlas.html">Atlas Roofing Company's</a> top-of-the-line 
        laminated shingle and are made to resemble a 
        traditional wood shake. The shingles are made with random cuts so they have a natural 
        appearance. Pinnacle shingles are available in a range of colors.
    </p>
  
    <h3>History</h3>
    <p>
        Atlas began production of shingles 
        in 1981 at their Atlanta, Georgia facility. Since then, Atlas has expanded their product 
        offerings. Atlas has expanded their manufacturing efforts to 19 facilities throughout the 
        world.
    </p>
    <p>
        Atlas maintains its corporate headquarters in Atlanta, Georgia, but has regional sales 
        offices throughout the country.
    </p>
    <h3>Warranty</h3>
    <p>
        Pinnacle shingles are available with either a 35- or 45-year limited warranty. Some of the 
        shingles also carry an Algae Resistant warranty. In general, the warranties are as follows:
        </p><ul>
            <li>The warranties cover manufacturer's defects and will offer replacement materials 
                for damage. They do not cover labor costs or damage that is a result of: improper 
                installation, poor roof ventilation, home settling, improper maintenance, or acts 
                of God (hurricanes, earthquakes, lightning, wind speeds in excess of stated warranty).</li>
            <li>The warranty covering algae-related damage applies only to those shingles that are 
                specifically algae-resistant shingles (ARS). The product is warranted for 10 years 
                against algae.</li>
            <li>Each warranty covers wind damage for winds up to 70 and 80 mph. This limited wind 
                warranty extends to the first 5 years after installation. </li>
        </ul>
    <p></p>
