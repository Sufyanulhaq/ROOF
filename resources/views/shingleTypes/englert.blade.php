    <p>
        Englert, Inc. is a manufacturer of metal roofing materials for both residential and commercial 
        applications. Englert uses stone-coated steel crafted into the look of traditional roofing 
        materials: shakes, tiles, or shingles. They use an acrylic-bonded stone chip finish, which is 
        adhered to the metal shingles. Englert also makes a single-seamed metal roof. The shingles 
        and the seamed metal roofs are available in several colors. 
    </p>
   
    <p>
        Englert markets their shingle under the Decra brand name -- Englert Decra Shingles, Shingles Plus, 
        and Decra Shakes. The shingles resemble traditional roofing shingles, while Decra Shingles Plus 
        are thicker and provide a more dimensional look. The Shingle Plus series are used in traditional 
        construction that requires a heavier roofing appearance, such as slate or tile.
    </p>
    <p>
        Englert shingles are rated a Class 4 for Impact resistance from Underwriters Laboratories, 
        require low maintenance, and don't split, crack, or rot. They are rated Class A for 
        <a href="/definitions/fire_ratings.html">fire resistance</a>.
    </p>
