    <p>
        Hanson Roof Tile, Inc. specializes in concrete roofing tiles. They primarily serve the American Southwest 
        and the state of Florida. They 
        custom-blend colors to create unique unique looks. Their products include:
        </p><ul>
            <li><b>Regal</b> -- A typical southwestern adobe barrel style</li>
            <li><b>Hacienda</b> -- Also adobe style, but narrower and longer </li>
            <li><b>Weathered Shake</b> -- Manufactured to imitate the look of a worn wooden shake</li>
            <li><b>Slate</b> -- Weathered-looking tiles with color striations</li>
            <li><b>Olde World Slate</b> -- Flat slate tile applied in several color combinations</li>
            <li><b>Everwest</b> -- Longish, narrow, somewhat weathered-looking tile</li>
            <li><b>Victorian Shake</b> -- Square earth-tone tiles with appearance of water marking</li>
            <li><b>Venetian</b> -- Another rolled tile in brighter tones</li>
