    <h3>Corporate History</h3>
    <p>
        Atlas Roofing Corp. began production of asphalt shingles in Meridian, Miss.,, in 1982. Since then, Atlas has expanded its manufacturing efforts 
        to 17 facilities throughout North America and its product offerings worldwide. The company has four divisions: Residential, Commercial, Web 
        Technologies and Atlas EPS.
    </p>
    <p>
        Atlas' product line also includes foam insulations. Atlas acquired Falcon Foam (producers of foam insulation) from 
        <a href="/shingles/brands/owens-corning.html">Owens Corning</a> in 2000.  Falcon now markets that product under the Atlas EPS name. 
    </p>
    <h3>Product Lines</h3>
    <p>
        Atlas offers two distinct categories of shingles: architectural and 3-tab. Architectural shingles feature a three-dimensional design. These 
        shingles are made of more than one layer of material, which enhances the overall appearance of the shingle and makes it more closely resemble 
        a natural shake or slate shingle. The extra layers are often composed of layers of laminate. Atlas makes architectural shingles under the 
        following product lines: Pinnacle, StormMaster, ProLAM and GlassMaster. Many of the company's shingles feature Scotchguard for algae resistance.
    </p>
    <p>
        Three-tab shingles have two notches cut into the lower edge of the shingle. This gives the shingle the appearance of being three smaller 
        shingles, instead of one large shingle. Atlas makes 3-tab shingles under the GlassMaster and Tough-Glass brand names. 
    </p>
