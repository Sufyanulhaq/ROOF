    <h3>Warranty</h3>
    <p>
        Warranties on Atlas shingles vary by product line. Consumers can select shingles that carry anywhere from a 30-year to lifetime limited warranty. 
        Some of the shingles also carry an "algae resistance" warranty. In general, the warranties are as follows: 
    </p>
    <ul>
        <li>The warranties cover manufacturer's defects and will offer replacement materials 
    	for damage, not covering labor costs. They do not cover damage that is a result 
    	of: improper installation, poor roof ventilation, home settling, improper 
    	maintenance, or acts of God (hurricanes, earthquakes, lightning, wind speeds 
    	in excess of stated warranty).</li>
        <li>The warranty covering algae-related damage applies only to algae-resistant 
    	shingles (ARS). ARS-treated shingles are warranted for 10 years against algae. </li>
        <li>Each warranty covers wind damage for winds up to the specified mph in the initial warranty. Wind warranties are valid for 5 to 15 years from 
    	the initial installation, depending on the product.</li>
    </ul>
