    <p>
        Building Products of Canada Corp.(BP) makes <a href="/shingles/asphalt.html">asphalt shingles</a> and asphalt roofing rolls from felt 
        produced in its own company mills. It also manufactures fiberglass, slate and laminate shingles. Its laminated fiberglass shingles are 
        sold under the brand names Harmony, Everest and Eclipse. 
    </p>
  
    <h3>History of BP's Manufacture of Shingles</h3>
    <p>
        BP was formed as Building Products Ltd. in 1925 when Bird and Son and Ruberoid Co. merged. Both Canadian companies were active in 
        roofing and other home products. Imperial Oil purchased Building Products in 1964, and continued to expand its product line. In 1987 
        EMCO Building Products Corp. bought the company with the goal of concentrating on roofing and wood fiber products. In 2007, EMCO 
        changed its name to the BP name by which it is known today. 
    </p>
    <h3>Product Lines</h3>
    <p>
        In its residential product line-up, BP offers an Architectural Collection of laminate shingles, as well as a Classic Collection of 
        3-tab shingles. Among the laminate options is the Manoir, which features deep shadows to create a slate look; the Everest, a 
        double-layer laminate with high-contrast colors; and the Mystique, a dimensional double-layer laminate in "vintage" colors.
    </p>
    <p>
        Three-tab products include the Mirage, which creates the look of a layered shake shingle; the Yukon, a fiberglass option with heavy 
        shadow bands; and the Dakota, which offers a classic look in numerous colors.
    </p>
    <p>
        The company also offers a line of specialty shingles, the Meridian, for hip and ridge applications. 
    </p>
