    <h3>Warranty</h3>
    <p>
        Both DaVinci slate and shake shingles carry a 50-year limited warranty, the first ten years of which are non-prorated. The warranty covers 
        manufacturers' defects and warrants against color fading. Wind damage is warranted only for the first ten years, and only up to 90 mph. 
        The terms of the warranty can be transferred to a new homeowner, although proof of purchase of the shingles is required for any claims. 
