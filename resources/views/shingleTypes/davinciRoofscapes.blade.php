    <p>
        DaVinci slate shingles are 1/2-inch thick and available in 23 colors; the shake shingles are 5/8-inch thick and available in numerous 
        color options. In addition to these two multi-width options, the company also offers its Valoré slate, Valoré shake and Bellafort
        single-width shingle products. The shingles carry a Class A <a href="/definitions/fire_ratings.html">fire rating</a>, a Class 4 
        impact resistance rating and are rated to withstand winds up to 110 mph. 
    </p>
    <p>
        DaVinci slate shingles received HGTV's Most Valuable Product award in 2004. The shake shingles received the award in 2005. The 
        company also was given the 2004 MVP award by Building Products and Hanley-Wood Publishers. 
    </p>
    <h3>History of DaVinci Roofscapes</h3>
    <p>
        Kansas-based DaVinci Roofscapes was founded by John Humphreys in 1999. Humphreys' family owned an asphalt shingle business. He used his 
        experience in the shingle manufacturing business to create a high-end synthetic shingle that could be used as a replacement for traditional 
        roofing materials. In 2006, private equity group TGP Investments LLC began its investment in the company, at which point DaVinci expanded 
        beyond its homeowner customer base to reach out to contractors. 
    </p>
