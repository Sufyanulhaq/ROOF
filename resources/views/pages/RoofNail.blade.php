    <h1>What is a Roofing Nail?</h1>
    <a href="{{url('/roof/def')}}">More roofing definitions</a><br>    <p>
	It may seem easy enough to pick up any old nails at the hardware store when re-roofing a house, but in fact, this
	home improvement project requires specialty nails intended for roofs. Roofing nails are different from drywall
	nails and masonry nails and finishing nails. They are specifically created for optimal effectiveness when used in
	roofing projects. 
    </p>

    <p>
	Roofing nails are used to attach shingles, tiles, and other roofing materials to felt or wood. They have larger
	heads than other nail types to keep shingles from popping over them and extremely sharp points, sometimes diamond
	tipped, for easier penetration of shingles. 
    </p>
    <h3>Roofing Nail Classifications</h3>
    <p>
	Roofing nails come in hand driven or coiled forms. Hand driven, as the name suggests, are for nails that will be
	hammered into place. Coiled nails are for use in a nail gun. Most commonly, roofing nails are available in one or
	two inch lengths. Other sizes may need to be special ordered. 
    </p>
    <h3>Nail Composition</h3>
    <p>
	There are different types of nails and each has merits depending on what they're being used for. Aluminum or
	stainless steel nails are probably some of the strongest nails and are the best for wood shingles. Copper nails,
	however, would be better for a slate roof. The most effective roofing nails for the most common type of shingle -
	asphalt - is galvanized steel or aluminum. Hot dipped galvanized nails are the sturdiest type of galvanized steel
	nail. 
    </p>
    <img src="{{asset('asset/images/roofing-nails.jpg')}}" alt="Roofing Nails" class="imgr">
    <h3>How to Buy Roofing Nails</h3>
    <p>
	Roofing nails are usually sold by the pound, though the boxes will often list quantity as well. While it is hard
	to know exactly how many nails a particular project will require, 3 to 4 nails per shingle is a good estimate.
	It's a good idea to always buy more than you think you will need. 
    </p>
    <p>
	Nails, like other roofing materials, are usually warranted for a certain number of years. Make sure the nail
	warranty matches the other materials you are using. For example, don't buy 10-year nails to use with 30-year
	shingles - unless you want to be re-roofing in 10 years, despite your investment in the higher quality shingles!
    
    </p>
