    <h1>What is Underlayment?</h1>
    <a href="{{url('/roof/def')}}">More roofing definitions</a><br>    <p>
	Most people know that every house needs a good foundation, but what about a roof? <b>Underlayment</b>, an 
	extra layer of protection on top of the sheathing, has become so important that 
	<a href="{{url('/shingles')}}">shingle manufacturers</a> often
	require it to validate their warranties. Additionally, many state and city codes deem it necessary to meet 
	fire, weather, puncture, and other protective standards. The main purpose of an underlayment is to provide 
	an extra layer to safeguard a home from extreme weather and the elements.
    </p>

    <p>
	Traditionally, underlayment has been constructed from felt. Also known as felt paper, it bonds with asphalt 
	via coating or saturation in order to form an extra barrier between the roof and the house. Felt varies by 
	weight and costs less than other underlayments. Although more widely used than some of the newer materials, 
	felt is less resistant to moisture problems, overheating, and tearing.
    </p>
    <p>
	Two new materials are growing in popularity in their use in underlayment (although they typically 
	cost more than more felt paper.) 
	The first is synthetic polymers, which are billed as holding up under all sorts of abuse and being safer for 
	roofers to work with. They also resist mold and water damage and weigh less than other substances. The other 
	roofing trend is the growing popularity of "complete roofing systems". These include an integrated  weather barrier, 
	underlayment, and shingles or tiles. Manufacturers reward buyers of these systems with longer warranties that 
	offer better coverage.
    </p>
    <p>
	A fourth option is the self-adhesive underlayment. It forms a water and ice barrier in areas affected by 
	extreme weather conditions. It may be described as an underlayment beneath another underlayment, because a 
	synthetic or felt layer is applied over its top and before the application of shingles or tiles. This layer 
	is heavy duty and seals seamlessly with no nails or staples. It also closes tightly around any fasteners used 
	to install the rest of the roof.
    </p>
    <p>
	If you plan to build a new home or replace an old roof, you should carefully consider your options for underlayment materials and 
	methods. Since there are more options available and the industry keeps growing, you'll want to make your 
	decision based on your budget, the number of years you plan to live in the house, the climate, and the design 
	of your house.
    </p>
