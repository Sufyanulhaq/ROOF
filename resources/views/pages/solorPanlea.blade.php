    <h1>How to Buy Solar Panels</h1>
    <div>
        <ul id="navlist">
    	<li><a href="/solar-panels/" id="current">How to Buy Solar Panels</a></li>
    	<li><a href="/solar-panels/reviews/">Solar Panel Reviews</a></li>
        </ul>
    </div>
    <p>
        Switching to <b>solar panels</b> or <a href="/solar-panels/solar-tiles.html">solar tiles</a> to meet your home's 
        energy requirements can prove to be a valuable investment. 
        Solar power can help you reduce your electric bill and contribute to saving the environment. 
        It can also increase the value of your property.  
    </p>

    <p>
        On the flip side, the US Government 
        <a href="https://www.energy.gov/eere/solar/homeowners-guide-going-solar">notes</a> that
        the amount you save depends "...on the electricity rates set by your utility and how much the utility will compensate you for the excess solar energy you send back to the grid."
    </p>
    <p>
        Solar panel modules can be installed or integrated on 
        to your roof or on the ground. When the sunlight comes in contact with the solar panels, it is converted 
        into power. The power goes through a solar inverter, which converts it into alternating current and passes 
        it on to your home through a service panel. If you produce more energy than you need, the excess can be run back 
        through your meter into your electric company's grid and directly reduce your electric bill!
    </p>
    <h2>Collect Some Basic Information</h2>
    <p>
        Before making any major investment, you need to do some basic research.  In this case, you need to collect
        some basic information:
        </p><ol>
    	<li>How much electricity do you use on a daily basis? Include your refrigerator, TV, lights, and other appliances.</li>
    	<li>How many hours of direct sunlight do you get each day?</li>
        </ol>
        The contractor that you hire to design and install your system will help you with these calculations.  However,
        this page explains how to calculate <a href="/solar-panels/calculate-electricity-usage.html">how much electricity you use</a>
        and this page explains how to calculate <a href="/solar-panels/calculate-direct-sunlight.html">how much direct sunlight you receive</a>.
    <p></p>
    <p>
        Another issue to consider is the risk of <a href="/solar-panels/hail-damage.html">hail damage</a>.  If hail is common where
        you live, there are some basic tactics you can use to minimize your risk.
    </p>
    <h2>How much Solar Power do you Want?</h2>
    <p>
        Most residential solar panel installations are designed to offset a portion of the electricity that your home or
        office consumes -- but not all.  That's because it is often dramatically more expensive to generate 100% of your 
        electricity needs than to generate 80% of them.  
    </p>
    <p>
        Consider the following: suppose that in an average month, you
        can meet 110% of your electrical needs from 20 meters of solar panels.  However, every couple of years, you have an
        odd month where the weather conspires against you and the combination of snow and clouds means that you can only generate
        30% of your electrical needs.  If you are designing a system to meet 100% of your needs, you now must look
        into battery backups, or dramatically larger arrays of solar panels.  Your costs have increased substantially just 
        to handle the odd month every couple of years.
    </p>
    <h2>The Payback Period </h2>
    <p>
        The payback period is the amount of time it will take for your system to pay for itself in energy cost savings. 
        Payback is calculated based on the current energy bill and the cost of your system. This will vary among systems. 
        Small systems may have a payback period of 15 years while larger systems may have a 12-year payback period. The 
        life of your solar panels typically ranges from 20 - 40 years, with manufacturers offering a 25-year warranty 
        across the industry. 
    </p>
    <h2>Hiring a Contractor to Install your Solar Panels</h2>
    <p>
        As is probably clear to anyone that has read this far, the contractor you choose will have a substantial impact
        on the success of your installation.  Here are several suggestions for how to choose the best contractor:
        </p><ul>
    	<li>Get bids from several contractors</li>
    	<li>Ask each contractor if they have experience installing grid-connected solar systems.  (A grid-connected
    	    system is one where you can sell-excess power back to your utility company.)</li>
    	<li>Ask how many years of experience they have installing solar panel systems (not just solar hot water systems)</li>
    	<li>Make sure that the contractors have appropriate licenses.  Check with your Town Hall for local requirements.</li>
    	<li>Ask for references, and then check them.</li>
        </ul>
