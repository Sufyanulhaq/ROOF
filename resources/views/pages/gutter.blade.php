    <h1>How to Buy Gutter Guards </h1>
    <img src="{{asset('asset/images/gutter-without-gutter-guard.jpg')}}" alt="gutter without a gutter guard" class="imgr">
    <p class="subscript">
	Read <a href="gutter-guards/reviews.html">Gutter Guard Reviews</a>
    </p>
    <p>
	There are two main types of rain gutters:  open or covered. Open systems are made from tubing that is open to the 
	elements. The other style uses a device over the gutters designed to prevent clogging. You may see them referred 
	to as "leaf guards", "gutter covers" or "gutter guards."  Since rain gutters exist to protect buildings from water damage, water 
	must be able to flow freely through them.  Any material that blocks or slows the flow of water through gutters 
	defeats their purpose. Therefore, gutter guards can be very useful. 
    </p>

    <h3>Who needs gutter guards?</h3>
    <p>
	Consider the following issues when deciding if your house will benefit from gutter guards. 
	</p><ul>
	    <li> <b> Debris:</b> First evaluate how much organic material comes down onto your roof. For instance, if your home 
		is surrounded by deciduous trees that lose their leaves in autumn, or evergreens whose needles fall out, you're 
		likely to collect more debris than someone who lives near palm trees.  If your region is subject to winds, the 
		problems may worsen. Collected leaves, twigs, and needles that sit in gutters long enough to dry out may also 
		create fire danger.</li>
	    <li><b> Maintenance:</b> Another reason homeowners choose gutter guards is they require less cleaning than open 
		gutter systems.  While many gutter protection systems claim to eliminate all future cleaning, home improvement 
		experts generally agree that rain gutters of all types require at least some degree of maintenance.  However, 
		if you are older, disabled or if your rooflines are difficult to access, gutter guards may reduce the amount of 
		maintenance either for you or someone you hire.</li>
	    <li> <b> Installation:</b> Ease of installation is something else to consider.  If you are going to install a gutter 
		system yourself, a closed system may be more complex than an open one.  Or, an installer may charge more for one or 
		the other.</li>
	</ul> 
    <p></p>
    <h3>Types of Gutter Guards</h3>
    <p>
	It would be great if there were only one type of gutter guard, but it isn't that easy. Once you decide to purchase gutter 
	guards, you'll find there are different styles to choose from. Here's a brief overview of what's available.
	</p><ul>
	    <li><b>Snap-in:</b> This guard system snaps into existing gutters, eliminating the need to remove current installations. 
		They provide a cover to block leaves and other debris.</li>
	    <li><b>Solid Cover:</b> These have holes or slits on the vertical side. Water adheres to the top, flows over and out 
		through the holes, leaving debris on top where it should eventually fall off. Debris that's small enough to fit 
		through the holes should travel out through the gutter and through the downspout.</li>
	    <li><b>Screen or Mesh:</b> These covers are caulked onto the back of the gutter and over the top, providing a filter 
		to keep large debris out of the system.</li>
	    <li>Gutter Shingle: This type attaches under the roofing and extends out over the gutters to keep debris out.</li>
	</ul>
    <p></p>
    <p>
	It's important to note that many gutter protection systems claim to eliminate all future cleaning. Home improvement 
	experts generally agree that rain gutters of all types require at least some degree of maintenance including cleaning out 
	debris. However, covered systems make upkeep easier.
    </p>
    <h3>Gutter Foam</h3>
    <p>
	A newer product, gutter foam, offers a different take on gutter protection. Manufacturers and distributors advertise 
	this product as porous enough for water to flow through but dense enough to block leaves and larger debris from entering 
	the gutters. They claim that small particles such as dirt and gravel will flow right through the foam and out the downspout 
	with the water. They also say that the chemicals in the foam prevent mold, mildew, and other biological damage, and that 
	the product is freeze and fire resistant. 
    </p>
