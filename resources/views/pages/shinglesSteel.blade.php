    <p>
	Steel shingles are becoming increasingly popular, in part because they are far more lightweight than traditional roofing methods, 
	are more fire resistant than other methods. They also resist mildew, splitting, cracking, curling, or rotting, and are often made 
	from recycled materials.  Steel roofs are also reflective, they tend to reflect sun rays back and reduce overall cooling needs 
	inside the house.  They also transfer heat evenly across the roof . in cold climates, they tend to aid in snow melting so there 
	is less overall snow buildup.  Many insurance companies offer a discount for homes that are roofed in steel shingles.
    </p>
    <p>
	Steel shingles are available in a variety of textures and colors.  
    </p>
    <h3>How are Steel Shingles made?</h3>
    <p>
	Various metals (aluminum, tin) or fiberglass are used as a core for steel shingles.  The core is shaped using an appropriate mold, and 
	then the core is sprayed with steel powder coating or dip coated in stainless steel.  Often, the core of the shingle is made with 
	recycled metals or plastics.  
    </p>
    <h3>How long do Steel shingles last?</h3>
    <p>
	Most steel shingle manufacturers warranty their roofing materials for 20, 30 or 50 years.  You can expect a steel roof to last 
	at least 50 years, possibly more if the roof is properly maintained.   
    </p>
    <h3>Common problems with Steel Shingles</h3>
    <p>
	Steel shingles are very durable and lightweight.  They aren't the ideal solution for every geographic area though, especially 
	areas with high winds or those that are hurricane prone.  Steel roofs are very stable, but like almost all roofing methods, 
	can be damaged by hail.  Steel shingles can be installed over existing asphalt roofs without adding excess weight burden to the home. 
    </p>
    <p>
	While steel shingles are lightweight and often require less shingles than asphalt shingles, they tend to be more expensive 
	than many methods.  Metal shingles are usually ten times the price of an asphalt shingle. Steel shingles can be applied by 
	the home owner, although many home owners experience difficulty when it comes to finishing the edges.  It may be best to 
	have an experienced roofer on hand when working on the project.
