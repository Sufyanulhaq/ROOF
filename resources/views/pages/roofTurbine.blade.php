    <h1>What is a Roof Turbine Vent?</h1>
    <p>
	A roof turbine vent uses specially shaped vanes to catch the wind and ventilate an attic. They are sometimes
	called whirligigs because they look like the child's toy of the same name, and they can be seen rotating atop
	residential, industrial, and commercial buildings. Roof turbine vents are often dome-shaped, non-electric vents
	that work in combination with soffit vents to suck out stale, warm and/or moist air from the attic. Consumers
	seeking to purchase turbine vents should look for ones made of aluminum that won't rust. 
    </p>
 
    <h3>How Do Roof Turbine Vents Work?</h3>
    <p>
	The wind caught in a roof turbine vent causes the mechanism to turn which pulls hot air from the attic or pulls in
	cool air from outdoors. They require winds of five miles-per-hour on average to function and are not recommended
	in areas where weather patterns don't regularly produce wind. 
    </p>
    <h3>What Problems Do Roof Turbine Vents Solve?</h3>
    <p>
	A steady flow of fresh air entering the attic through the soffit vents keeps the space dry and cool. A dry attic
	prevents mold from growing, wood from rotting, and ice from building up - all of which can lead to damage to the
	roof and the underlying building. 
    </p>
    <h3>What Problems Do Turbine Vents Cause? </h3>
    <p>
	While performing a vital function, roof turbine vents also have some disadvantages. For one, small animals can
	enter an attic through the turbine vents and build nests in the insulation to winter in. Small animals or tree
	branches can also damage the pliable metal of the turbines and prevent proper rotation and effective ventilation.
	Additionally, some consumers may worry about the greater heat loss that incurs when using this type of vent
	compared to using ridge vents. Lastly, snow, rain, and sleet can enter through the vent when it isn't rotating
	and they've also been known to blow off in very high winds.
    </p>
