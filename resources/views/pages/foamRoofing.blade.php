    <h1>What is Foam Roofing?</h1>
    <p>
	Spray polyurethane foam (SPF) has become a popular choice of materials especially on flat roofs. Sprayable foam is
	created by the combination of two component mixtures that come together at the tip of a gun. The resulting
	product is expanding foam that can be applied on practically any surface. It is used on low-slope and flat roofs
	for both residential and commercial buildings. SPF is considered energy efficient, less expensive than other
	materials, and is available in a variety of colors. 
    </p>
 
    <h3>How Foam Roofing Works </h3>
    <p>
	Foam roofing is available as a spray and so provides a seamless product that can protect buildings from the
	elements. The application technique permits the foam to directly adhere to the roof with no flashing or seams,
	thus avoiding many of the problems associated with other roofing products that permit leaks to develop.
    </p>
    <p>
	Spray foam roofing is manufactured from a variety of chemical elements which affect the safety of installation,
	material performance, and environmental green qualities. Base products include polyisocyanurate, tripolymer,
	polystyrene, and closed- or open-cell polyurethane such as icynene.
    </p>
    <p>
	Professional installation of SPF is important to prevent any number of installation mishaps. Homeowners generally
	do not have the skills and equipment to install a foam roof themselves. 
    </p>
    <h3>Warranties on Foam Roofing</h3>
    <p>
	Warranties usually cover manufacturing defects. They will also guarantee that the foam meets published physical
	properties when applied, cured, and tested according to the company's standard procedures. Liability is often
	limited to replacing the materials within ten years of installation. Warranties do not cover damages caused by
	natural disasters, intentional or negligent acts, deterioration of building components (such as the substrate),
	or harmful chemicals which may come into contact with the SPF.
    </p>
    <h3>Common Problems with Foam Roofing</h3>
    <p>
	As with any roofing surface, punctures in the material can occur and cause water to seep in under the foam,
	through to the insulation, and possibly to substrate layers. If the application of the SPF occurs when the roof
	isn't completely dry, blisters may develop leading the foam to separate from the roof. Sometimes excessive foot
	traffic on an SPF roof can cause scrapes or tears in the foam. Always visually inspect the roof after anyone has
	been walking around up there. 
    </p>
    <h3>Environmental Impact </h3>
    <p>
	Some foam roofing products are manufactured without the use of solvents and formaldehydes, and therefore, do not
	have the negative indoor air quality sometimes associated with those chemicals. SPFs that use water as a blowing
	agent are more environmentally friendly than those that do not; foams that do not contain chlorofluorocarbons
	(CFC's) are also better for the environment. Spray foam cannot be re-used and ends up in landfills when it needs
	to be removed. 
    </p>
